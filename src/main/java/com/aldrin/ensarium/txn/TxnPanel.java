    private RoundedScrollPane buildCenterPanel() {
        table.setComponentPopupMenu(popupMenu);

        table.addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent e) {
                selectRowOnPopup(e);
            }

            @Override
            public void mouseReleased(MouseEvent e) {
                selectRowOnPopup(e);
            }

            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 2 && table.getSelectedRow() >= 0) {
                    viewSelectedTransactionDetails();
                }
            }
        });

        RoundedScrollPane scrollPane = new RoundedScrollPane(table);
//        scrollPane.setBorder(BorderFactory.createTitledBorder("Inventory Transactions"));
        return scrollPane;
    }

