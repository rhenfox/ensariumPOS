    private void buildPopupMenu() {
        JMenuItem miViewDetails = new JMenuItem("View Details");
        JMenuItem miRefresh = new JMenuItem("Refresh");
        miViewDetails.setIcon(iconSearch);
        miRefresh.setIcon(iconRefresh);

        miViewDetails.addActionListener(e -> viewSelectedTransactionDetails());
        miRefresh.addActionListener(e -> loadTransactions());

        popupMenu.add(miViewDetails);
        popupMenu.addSeparator();
        popupMenu.add(miRefresh);
    }

    private void selectRowOnPopup(MouseEvent e) {
        if (e.isPopupTrigger()) {
            int row = table.rowAtPoint(e.getPoint());
            if (row >= 0) {
                table.setRowSelectionInterval(row, row);
            }
        }
    }

    private void loadTransactions() {
        String keyword = searchField.getText();
        int limit = (Integer) jSpinner1.getValue();
        statusLabel.setText("Loading...");

        new SwingWorker<List<InventoryTxn>, Void>() {
            @Override
            protected List<InventoryTxn> doInBackground() throws Exception {
                return dao.findTransactions(keyword, limit);
            }

            @Override
            protected void done() {
                try {
                    List<InventoryTxn> rows = get();
                    tableModel.setRows(rows);
                    statusLabel.setText(rows.size() + " row(s) loaded.");
                } catch (Exception ex) {
                    statusLabel.setText("Load failed.");
                    SwingUtils.showError(TxnPanel.this, "Failed to load inventory transactions.", ex instanceof Exception ? (Exception) ex : null);
                }
            }
        }.execute();
    }

    private void viewSelectedTransactionDetails() {
        int selectedRow = table.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a transaction first.", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        InventoryTxn txn = tableModel.getRow(selectedRow);
        if (txn == null) {
            return;
        }

        Window owner = SwingUtilities.getWindowAncestor(this);
        DonutLoadingDialog loadingDialog = new DonutLoadingDialog(owner, "Loading transaction details...");
        InventoryTxnDetailsDialog dialog = new InventoryTxnDetailsDialog(owner);

        new SwingWorker<Void, Void>() {
            private Exception error;

            @Override
            protected Void doInBackground() {
                try {
                    dialog.loadTransaction(txn.getId());
                } catch (Exception ex) {
                    error = ex;
                }
                return null;
            }

            @Override
            protected void done() {
                loadingDialog.dispose();
                if (error != null) {
                    if (error instanceof SQLException sqlEx) {
                        SwingUtils.showError(TxnPanel.this, "Failed to load transaction details.", sqlEx);
                    } else {
                        SwingUtils.showError(TxnPanel.this, "Failed to load transaction details.", error instanceof Exception ? error : new Exception(error));
                    }
                    return;
                }
                dialog.setVisible(true);
            }
        }.execute();

        loadingDialog.setVisible(true);
    }

    private void hideColumn(int i) {
        table.getColumnModel().getColumn(i).setMinWidth(0);
        table.getColumnModel().getColumn(i).setMaxWidth(0);
        table.getColumnModel().getColumn(i).setPreferredWidth(0);
        table.getColumnModel().getColumn(i).setResizable(false);
    }

    private void installTableHover() {
        table.addMouseMotionListener(new java.awt.event.MouseMotionAdapter() {
            @Override
            public void mouseMoved(java.awt.event.MouseEvent e) {
                int row = table.rowAtPoint(e.getPoint());
                if (row != hoveredRow) {
                    hoveredRow = row;
                    table.repaint();
                }
            }
        });

        table.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseExited(java.awt.event.MouseEvent e) {
                if (hoveredRow != -1) {
                    hoveredRow = -1;
                    table.repaint();
                }
            }
        });
    }

    static class DonutLoadingDialog extends JDialog {

        private final DonutSpinner spinner;

        public DonutLoadingDialog(Window owner, String message) {
            super(owner, "Loading", Dialog.ModalityType.MODELESS);
            setUndecorated(true);
            JPanel panel = new JPanel(new BorderLayout(10, 10));
            panel.setBorder(BorderFactory.createCompoundBorder(
                    BorderFactory.createLineBorder(new Color(0xD0D7DE)),
                    BorderFactory.createEmptyBorder(16, 16, 16, 16)
            ));
            panel.setBackground(Color.WHITE);
            spinner = new DonutSpinner();
            JLabel lblMessage = new JLabel(message, SwingConstants.CENTER);
            lblMessage.setForeground(new Color(60, 60, 60));
            panel.add(spinner, BorderLayout.CENTER);
            panel.add(lblMessage, BorderLayout.SOUTH);
            setContentPane(panel);
            setSize(220, 180);
            setLocationRelativeTo(owner);
            spinner.start();
        }

        @Override
        public void dispose() {
            spinner.stop();
            super.dispose();
        }
    }

    static class DonutSpinner extends JPanel {

        private int angle = 0;
        private final Timer timer;

        public DonutSpinner() {
            setOpaque(false);
            setPreferredSize(new Dimension(90, 90));
            timer = new Timer(20, e -> {
                angle += 8;
                if (angle >= 360) {
                    angle = 0;
                }
                repaint();
            });
        }

        public void start() {
            if (!timer.isRunning()) {
                timer.start();
            }
        }

        public void stop() {
            if (timer.isRunning()) {
                timer.stop();
            }
        }

        @Override
        protected void paintComponent(Graphics g) {
            super.paintComponent(g);
            Graphics2D g2 = (Graphics2D) g.create();
            g2.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);
            int size = Math.min(getWidth(), getHeight()) - 14;
            int x = (getWidth() - size) / 2;
            int y = (getHeight() - size) / 2;
            g2.setStroke(new BasicStroke(10f, BasicStroke.CAP_ROUND, BasicStroke.JOIN_ROUND));
            g2.setColor(new Color(230, 235, 240));
            g2.drawArc(x, y, size, size, 0, 360);
            g2.setColor(new Color(0, 120, 215));
            g2.drawArc(x, y, size, size, angle, 110);
            g2.dispose();
        }
    }
